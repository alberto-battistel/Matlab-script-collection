function Z = RsChRctWd(V,f)
%**************************************************************************
%
%**************************************************************************
Rs = 10^V(1);
Ch = 10^V(2);
Rct = 10^V(3);
Wd = 10^V(4);

w = 2*pi*f;

Ydl = 1i*w*Ch;
Yct = (Wd./sqrt(1i*w) + Rct).^-1;

Z = Rs + (Ydl + Yct).^-1;
return